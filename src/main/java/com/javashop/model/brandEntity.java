package com.javashop.model;


public class BrandEntity {
    int id;
    String Name;

    public BrandEntity() {

    }

    public int getId() {

        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
